import Foundation

struct Membership: Codable {

    let current: Bool
    let groupRef: RefKey
    let creationTime: String?
    let expirationTime: String?
    
    enum CodingKeys: String, CodingKey {
        case current
        case groupRef = "group_ref"
        case creationTime = "creation_time"
        case expirationTime = "expiration_time"
    }
}
